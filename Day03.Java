package day03.java;
public class Day03Java {
    public static void main(String[] args) {
        /**
        * Day03.java
        * Contoh penggunaan komentar dalam Java
        */
        // Komentar satu baris: mencetak pesan ke layar
        System.out.println("Halo, dunia!");

        /*
         Komentar multi baris:
         Berikut ini contoh variabel dengan tipe data berbeda
         dan mencetaknya menggunakan println()
        */
        String nama = "Yugo Difan Ananta";
        int umur = 17;
        double tinggi = 170.5;

        System.out.println("Nama: " + nama);   // Menampilkan nama
        System.out.println("Umur: " + umur);   // Menampilkan umur
        System.out.println("Tinggi: " + tinggi); // Menampilkan tinggi
    }
}
