package yugodifan;
public class YugoDifan {
    public static void main(String[] args) {
        float f = 3.14f;          // Harus ada 'f' di akhir
        double d = 3.14159265359; // Double tidak perlu 'd' tapi bisa ditambahkan
        
        System.out.println("Nilai float: " + f);
        System.out.println("Nilai double: " + d);
    }
}
